static void
usage(void)
{
	fprintf(stderr, "usage: largeblob [-a cred_id] [-P pin] <device>\n");
	exit(EXIT_FAILURE);
}

static void
verify_blob(const unsigned char *ptr, size_t len)
{
	if (len != sizeof(largeblob))
		errx(1, "%s: %zu != %zu", __func__, len, sizeof(largeblob));

	if (memcmp(ptr, largeblob, sizeof(largeblob)) != 0)
		errx(1, "%s: memcmp", __func__);
}

static int
get_assertion(fido_dev_t *dev, fido_assert_t *assert, const char *pin)
{
	int r;

	/* client data hash */
	r = fido_assert_set_clientdata_hash(assert, cdh, sizeof(cdh));
	if (r != FIDO_OK)
		errx(1, "fido_assert_set_clientdata_hash: %s (0x%x)",
		    fido_strerr(r), r);

	/* relying party */
	r = fido_assert_set_rp(assert, "localhost");
	if (r != FIDO_OK)
		errx(1, "fido_assert_set_rp: %s (0x%x)", fido_strerr(r), r);

	/* extensions */
	r = fido_assert_set_extensions(assert, FIDO_EXT_LARGEBLOB_KEY);
	if (r != FIDO_OK)
		errx(1, "fido_assert_set_extensions: %s (0x%x)", fido_strerr(r), r);

	r = fido_dev_get_assert(dev, assert, pin);
	if (r != FIDO_OK)
		errx(1, "fido_dev_get_assert: %s (0x%x)", fido_strerr(r), r);

	return (r);
}

int
main(int argc, char **argv)
{
	fido_dev_t		*dev = NULL;
	fido_assert_t		*assert = NULL;
	const char		*pin = NULL;
	const unsigned char	*key_ptr;
	size_t			 key_len;
	unsigned char		*body = NULL;
	size_t			 len;
	int			 ch;
	int			 r;

	if ((assert = fido_assert_new()) == NULL)
		errx(1, "fido_assert_new");

	while ((ch = getopt(argc, argv, "P:a:")) != -1) {
		switch (ch) {
		case 'P':
			pin = optarg;
			break;
		case 'a':
			if (read_blob(optarg, &body, &len) < 0)
				errx(1, "read_blob: %s", optarg);
			r = fido_assert_allow_cred(assert, body, len);
			if (r != FIDO_OK)
				errx(1, "fido_assert_allow_cred: %s (0x%x)",
				    fido_strerr(r), r);
			free(body);
			body = NULL;
			break;
		default:
			usage();
		}
	}

	argc -= optind;
	argv += optind;

	if (argc != 1)
		usage();

	fido_init(0);

	if ((dev = fido_dev_new()) == NULL)
		errx(1, "fido_dev_new");

	r = fido_dev_open(dev, argv[0]);
	if (r != FIDO_OK)
		errx(1, "fido_dev_open: %s (0x%x)", fido_strerr(r), r);

	r = get_assertion(dev, assert, pin);
	if (r != FIDO_OK)
		errx(1, "get_assertion: %s (0x%x)", fido_strerr(r), r);

	if ((key_ptr = fido_assert_largeblob_key_ptr(assert, 0)) == NULL ||
	    (key_len = fido_assert_largeblob_key_len(assert, 0)) == 0)
		errx(1, "no associated largeblob key");

	r = fido_dev_largeblob_set(dev, key_ptr, key_len, largeblob,
	    sizeof(largeblob), pin);
	if (r != FIDO_OK)
		errx(1, "fido_dev_largeblob_set: %s (0x%x)", fido_strerr(r), r);

	r = fido_dev_largeblob_get(dev, key_ptr, key_len, &body, &len);
	if (r != FIDO_OK)
		errx(1, "fido_dev_largeblob_get: %s (0x%x)", fido_strerr(r), r);

	verify_blob(body, len);
	free(body);
	body = NULL;

	r = fido_dev_largeblob_remove(dev, key_ptr, key_len, pin);
	if (r != FIDO_OK)
		errx(1, "fido_dev_largeblob_remove: %s (0x%x)", fido_strerr(r), r);

	r = fido_dev_largeblob_get(dev, key_ptr, key_len, &body, &len);
	if (r != FIDO_ERR_NOTFOUND)
		errx(1, "fido_dev_largeblob_get: %s (0x%x)", fido_strerr(r), r);

	r = fido_dev_close(dev);
	if (r != FIDO_OK)
		errx(1, "fido_dev_close: %s (0x%x)", fido_strerr(r), r);

	fido_dev_free(&dev);
	fido_assert_free(&assert);

	exit(0);
}
